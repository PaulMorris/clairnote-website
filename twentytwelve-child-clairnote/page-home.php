<h1 id="home-h1" class="entry-title">Clairnote Music Notation</h1>
<h2 id="home-subtitle">An alternative notation system that makes music easier to read and understand.</h2>

<h2 class="home-h2">Explicit and Direct</h2>
<p class="home-bigtext">Clairnote gives each of the twelve notes of the chromatic scale
its own vertical position on the staff.
There are no indirect alterations by key signatures or accidental signs.
Play what you see and see what you play.</p>

<figure>
<img class="alignnone size-full" src="http://clairnote.org/images/Clairnote-chromatic-scale-quarter-notes-tmn.svg" alt="Chromatic scale in Clairnote and traditional music notation" />

<figcaption class="cn-caption">Chromatic Scale in Clairnote and Traditional Notation</figcaption>
</figure>

<h2 class="home-h2">Consistent and Intuitive</h2>
<p class="home-bigtext">Clairnote represents interval relationships clearly and consistently, making
them easy to recognize. Hear what you see and see what you hear.</p>

<figure>
<img class="alignnone size-full" src="http://clairnote.org/images/Clairnote-C-major-scale-whole-steps-half-steps-quarter-notes-tmn.svg" alt="C major scale in Clairnote and traditional music notation" />

<figcaption class="cn-caption">C Major Scale in Clairnote and Traditional Notation</figcaption>
</figure>

<p class="home-bigtext">Any given note looks the same in every octave.
Once you can read one octave you can read them all.  This makes it simpler
to identify notes and to read music that spans multiple octaves.</p>

<figure>
<img class="alignnone size-full" src="http://clairnote.org/images/Clairnote-octaves-C-major-quarter-notes-tmn.svg" alt="Octaves in Clairnote and traditional music notation" />

<figcaption class="cn-caption">Octaves in Clairnote and Traditional Notation</figcaption>
</figure>

<h2 class="home-h2">Solid and Hollow Notes</h2>

<p>Whether a note is solid or hollow is its most prominent visual feature,
so Clairnote uses this solid/hollow distinction to help convey <em>pitch</em> rather than duration.
The vertical positions on the staff alternate regularly between solid and hollow noteheads.
This makes individual notes easier to identify and interval
relationships easier to see.</p>

<figure>
<img class="alignnone size-full" src="http://clairnote.org/images/Clairnote-C-major-scale-major-thirds-minor-thirds-tmn.svg" alt="Thirds in the key of C Major in Clairnote and traditional music notation" />

<figcaption class="cn-caption">Thirds in the Key of C Major in Clairnote and Traditional Notation</figcaption>
</figure>

<p>Traditional rhythm symbols are used except half notes
are given a double stem to distinguish them from quarter notes.</p>

<img class="alignnone size-full" src="http://clairnote.org/images/Clairnote-half-notes-quarter-notes-brief.svg" alt="Half notes and quarter notes in Clairnote" />

<p class="aside">(<a href="http://clairnote.org/sn/">Clairnote SN</a> doesn't
use solid/hollow notes to convey pitch, offering full continuity with the
traditional rhythm system. See
<a href="http://clairnote.org/clairnote-dn-clairnote-sn/">Clairnote and
Clairnote SN</a>.)</p>

<h2 class="home-h2">Clefs and Octaves</h2>
<p>In traditional music notation the notes represented by the lines and
spaces of the staff
change depending on the current clef (treble, bass, alto, tenor, etc.).
Piano music entails reading in two different clefs at once.
In Clairnote the lines and spaces of the staff look the same in every octave and always mean the
same thing in every octave, so there is no need to learn to read different clefs.
Clairnote's <a href="http://clairnote.org/clefs/">clef symbols</a>
simply indicate the octave register of the staff.</p>

<img class="alignnone size-full" src="http://clairnote.org/images/Clairnote-clefs-and-octaves-comparison.svg" alt="Clefs and octaves in Clairnote and traditional notation" />


<h2 class="home-h2">Key Signatures and Accidental Signs</h2>
<p>In traditional music notation the notes on the staff may be altered
by accidental signs (sharps, flats, naturals, double sharps, double flats)
or by one of fifteen different key signatures that you have to learn and
keep in mind in order to play the correct notes. With Clairnote you
can simply play the notes as they appear on the staff and learn as you go.</p>

<img class="alignnone size-full" src="http://clairnote.org/images/Clairnote-and-traditional-key-signature-accidental-signs-brief.svg" alt="Key signature and accidental signs in Clairnote" />

<p>Clairnote&#8217;s <a href="http://clairnote.org/key-signatures/">key signatures</a> and
<a href="http://clairnote.org/accidental-signs/">accidental signs</a>
do not complicate the process of reading notes. They only provide <em>supplemental</em>
information &mdash; all the same information conveyed by traditional notation,
like the current key, when a note is an accidental (i.e. not in the current key),
and the different names of enharmonically equivalent notes.</p>

<figure>
<img class="alignnone size-full" src="http://clairnote.org/images/Clairnote-key-signatures-tmn.gif" alt="All key signatures in Clairnote and traditional music notation" />

<figcaption class="cn-caption">Different Keys and Key Signatures in Clairnote and Traditional Notation</figcaption>
</figure>


<h2 class="home-h2">Intervals and Chords</h2>
<p>In traditional music notation different intervals and chords may look the same
(e.g. major and minor thirds and triads).
In Clairnote the differences between intervals
are always clearly visible making it easy to see the relationships between
notes and understand the common patterns of music found in chords, scales, keys, etc.
Improvising and playing by ear involve playing by interval (by relative pitch),
so clearly seeing each interval as you play will help support learning these skills.
See <a title="Intervals" href="http://clairnote.org/intervals/">Intervals</a>,
<a title="Chords" href="http://clairnote.org/chords/">Chords</a>, and
<a title="Scales" href="http://clairnote.org/scales/">Scales</a> for more illustrations and discussion.</p>

<img class="alignnone size-full" src="http://clairnote.org/images/Clairnote-and-traditional-thirds-and-triads.svg" alt="Thirds and triads in Clairnote and traditional notation" />


